package zhongd.member.entity;

public class User {
	private String username;
	private int age;
	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return username;
	}

	public void setName(String name) {
		this.username = name;
	}
}
